package org.wikidata.wdtk.examples;

/**
 * 
 * Elhuyar Foundation form representation
 * 
 * @author Elhuyar Foundation
 *
 */
public class ElhuyarForm {

	private String kasua;
	private String forma;
	private String zkia;
	private String izaera; 
	
	public String getKasua() {
		return kasua;
	}

	public void setKasua(String kasua) {
		this.kasua = kasua;
	}

	public String getForma() {
		return forma;
	}

	public void setForma(String forma) {
		this.forma = forma;
	}

	public String getZkia() {
		return zkia;
	}

	public void setZkia(String zkia) {
		this.zkia = zkia;
	}

	public String getIzaera() {
		return izaera;
	}

	public void setIzaera(String izaera) {
		this.izaera = izaera;
	}

}
